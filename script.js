function calculateGrade(marks){
    let sum = 0;
    for(let index of marks){
        sum += index;
    }
    let size = marks.length;
    let mark = sum/size;

    if(mark >= 90 )return 'a';
    else if(mark >= 80)return 'b';
    else if(mark >= 70)return 'c';
    else if(mark >= 60)return 'd';
    else return 'F'
};
